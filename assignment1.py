# Python program able to calculate the BMI of a person whose weight is 78kgs and height 1.75 metres. (BMI= (weight)/(height squared))

weight = 78
height = 1.75
BMI = (weight) / (height**2)
print("The BMI of the person is", BMI)

# Find the area and perimeter of a rectangle whose length is 48cm and width 25cm.
# AREA
length = 48
width = 25
area = length * width
print("The area of the rectangle is",area)

# LENGTH
perimeter = 2 * (length + width)
print("The perimeter of the rectangle is", perimeter)

