# Python Data Types
# String - Set/series of characters that are inside quotes
name = "Moses Wachira"
print(name)
print(type(name))

# Numeric
# Int - Indicates the whole number in python, does not use quotes.
age = 21
print('The age of this person is:',age)
print(type(age))

# Float - Number with decimal.
coordinates = 15.4524
print("The coordinates are",coordinates)
print(type(coordinates))

